type LocaleTree = {
  readonly [key: string]: string | LocaleTree;
};

type LocalePatch = {
  readonly [key: string]: string | LocalePatch;
};

function mergeLocale<T extends LocaleTree>(base: T, patch: LocalePatch): T {
  const result: Record<string, string | LocaleTree> = {};

  for (const [key, value] of Object.entries(base)) {
    const patchedValue = patch[key];
    if (typeof value === "string") {
      result[key] = typeof patchedValue === "string" ? patchedValue : value;
      continue;
    }
    result[key] = mergeLocale(value, typeof patchedValue === "object" && patchedValue !== null ? patchedValue : {});
  }

  return result as T;
}

